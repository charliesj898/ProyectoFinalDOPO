package domain;

/**
 * Tipos de moneda disponibles en el juego.
 * YELLOW: moneda estandar que se debe recolectar para completar el nivel.
 * RED_SKIN, BLUE_SKIN, GREEN_SKIN: monedas que cambian temporalmente el skin del jugador.
 */
public enum CoinType {
    YELLOW,
    RED_SKIN,
    BLUE_SKIN,
    GREEN_SKIN
}
